package curso.generation.demo;

public class DatosAcceso {
	
	private String nombre;

	private int anioOrigen,anioFinal,mes;
	
	private String user, password;


	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getAnioOrigen() {
		return anioOrigen;
	}

	public void setAnioOrigen(int anioOrigen) {
		this.anioOrigen = anioOrigen;
	}

	public int getAnioFinal() {
		return anioFinal;
	}

	public void setAnioFinal(int anioFinal) {
		this.anioFinal = anioFinal;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
	
	

}
