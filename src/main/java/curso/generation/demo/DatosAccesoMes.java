package curso.generation.demo;

public class DatosAccesoMes {
	private int anioOrigen, anioFinal, mes;
	private String nombre;

	

	/**
	 * @return the mes
	 */
	public int getMes() {
		return mes;
	}

	/**
	 * @param mes the mes to set
	 */
	public void setMes(int mes) {
		this.mes = mes;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * @return the anioOrigen
	 */
	public int getAnioOrigen() {
		return anioOrigen;
	}

	/**
	 * @param anioOrigen the anioOrigen to set
	 */
	public void setAnioOrigen(int anioOrigen) {
		this.anioOrigen = anioOrigen;
	}

	/**
	 * @return the anioFinal
	 */
	public int getAnioFinal() {
		return anioFinal;
	}

	/**
	 * @param anioFinal the anioFinal to set
	 */
	public void setAnioFinal(int anioFinal) {
		this.anioFinal = anioFinal;
	}
}
